package p1214;

public class a051Fruit {		// 말만 과일이고, 이름과 색밖에 없다
	private String name;
	private String color;
	
	a051Fruit(String name, String color){
		this.name = name;
		this.color = color;
	}
	public String getName() {
		return name;
	}
	public String getColor() {
		return color;
	}
}